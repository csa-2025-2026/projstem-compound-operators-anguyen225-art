public class Problem1
{
    public static void main(String[] args)
    {
        int num = 24;
        System.out.println("initial number is " + num);

        num = num + 1;
        System.out.println("number is now " + num);

        num += 1;
        System.out.printf("number is now %d%n", num);

        num++;
        System.out.println("number is now " + num);

        num = num + 1;
        System.out.println("number is now " + num);

        num -= 1;
        System.out.printf("number is now %d%n",  num);

        num = num - 1;
        System.out.println("number is now " + num);

        num--;
        System.out.println("number is now " + num);

        num = num - 1;
        System.out.println("number is now " + num);


    }
}
